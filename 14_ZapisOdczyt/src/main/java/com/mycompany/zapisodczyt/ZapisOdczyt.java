    private void jButton_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_saveActionPerformed
        String firstName = jTextField_firstName.getText();
        String lastName = jTextField_lastName.getText();
        
        try {
            FileWriter fw = new FileWriter(file);
            fw.write(firstName+";"+lastName);
            fw.close();
        } catch (IOException ex) {
            System.out.println(ex);
        }
    }//GEN-LAST:event_jButton_saveActionPerformed

    private void jButton_loadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_loadActionPerformed
        try {
            Scanner sc = new Scanner(file);
            
            String line = sc.nextLine();
            String[] data = line.split(";");
            
            jTextField_firstName.setText(data[0]);
            jTextField_lastName.setText(data[1]);
            sc.close();
        } catch (FileNotFoundException ex) {
            System.out.println(ex);
        }
    }//GEN-LAST:event_jButton_loadActionPerformed


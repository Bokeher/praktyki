    private void start() {
        addPoint("###");
        clearBoard();
        changeGameStatus();
        changeCurrPlayer();
        if(gameState) {
            jL_points1.setText("");
            jL_points2.setText("");
            jL_points.setText("");
            
            player1Points = 0;
            player2Points = 0;
        }
    }
    
    private void changeGameStatus() {
        jTF_player1_nickname.setEditable(!gameState);
        jTF_player2_nickname.setEditable(!gameState);
        
        jB_changeSign.setEnabled(!gameState);
        
        jB_map_1.setEnabled(gameState);
        jB_map_2.setEnabled(gameState);
        jB_map_3.setEnabled(gameState);
        jB_map_4.setEnabled(gameState);
        jB_map_5.setEnabled(gameState);
        jB_map_6.setEnabled(gameState);
        jB_map_7.setEnabled(gameState);
        jB_map_8.setEnabled(gameState);
        jB_map_9.setEnabled(gameState);
        
        changeActionButtonText(gameState);
        
        gameState = !gameState;
    }
    
    private void changeActionButtonText(boolean bol) {
        if(bol) jB_start.setText("STOP");
        else jB_start.setText("START");
    }
    

    private void logic() {
        // X = 1, O = 2
        String winner = "none";
        int[][] t = new int[3][3];
        //y  x
        t[0][0] = textToInt(jB_map_1);
        t[0][1] = textToInt(jB_map_2);
        t[0][2] = textToInt(jB_map_3);
       
        t[1][0] = textToInt(jB_map_4);
        t[1][1] = textToInt(jB_map_5);
        t[1][2] = textToInt(jB_map_6);
       
        t[2][0] = textToInt(jB_map_7);
        t[2][1] = textToInt(jB_map_8);
        t[2][2] = textToInt(jB_map_9);
        
        //poziomo
        for(int i=0; i<3; i++) {
            int sum = t[i][0] + t[i][1] + t[i][2];
            if(sum == 3) winner = "X";
            else if(sum == 6) winner = "O";
        }
       
        //pionowo
        for(int i=0; i<3; i++) {
            int sum = t[0][i] + t[1][i] + t[2][i];
            if(sum == 3) winner = "X";
            else if(sum == 6) winner = "O";
        }
       
        //ukos
        int sum = t[0][0] + t[1][1] + t[2][2];
        if(sum == 3) winner = "X";
        else if(sum == 6) winner = "O";
       
        sum = t[2][0] + t[1][1] + t[0][2];
        if(sum == 3) winner = "X";
        else if(sum == 6) winner = "O";
       
        if(winner.equals("X")) {
            if(jL_player1_sign.getText().equals("X")) {
                if(jTF_player1_nickname.getText().length() == 0) winner = "Gracz 1";
                else winner = jTF_player1_nickname.getText();
            } else {
                if(jTF_player2_nickname.getText().length() == 0) winner = "Gracz 2";
                else winner = jTF_player2_nickname.getText(); 
            }
        } else if(winner.equals("O")) {
            if(jL_player1_sign.getText().equals("O")) {
                if(jTF_player1_nickname.getText().length() == 0) winner = "Gracz 1";
                else winner = jTF_player1_nickname.getText();
            } else {
                if(jTF_player2_nickname.getText().length() == 0) winner = "Gracz 2";
                else winner = jTF_player2_nickname.getText(); 
            }
        }
        if(!winner.equals("none")) win(winner);
        else{
            sum = 0;
            for(int i = 0; i < 3; i++) {
                if(t[i][0] + t[i][1] + t[i][2] < 10) sum++;
            }
            if(sum == 3) win("Remis");
        }
        
    }
   
    private void win(String winner) {
        if(!winner.equals("Remis")) {
            addPoint(winner);
            winner = "Wygrał "+winner;
        }
        JOptionPane.showMessageDialog(null, winner, "", JOptionPane.PLAIN_MESSAGE);
        clearBoard();
    }
    
    private void addPoint(String winner) {
        if(jTF_player1_nickname.equals(winner)) player1Points++;
        else if(jTF_player2_nickname.equals(winner)) player2Points++;
        else if(jL_A.equals(winner)) player1Points++;
        else if(jL_B.equals(winner)) player2Points++;
        
        String player1Nick = "Gracz 1";
        String player2Nick = "Gracz 2";
        
        if(jTF_player1_nickname.getText().length() > 0) player1Nick = jTF_player1_nickname.getText();
        if(jTF_player2_nickname.getText().length() > 0) player2Nick = jTF_player2_nickname.getText();
        
        if(winner.equals(player1Nick)) player1Points++;
        else if(winner.equals(player2Nick)) player2Points++;
        
        jL_points1.setText(player1Nick+": "+player1Points);
        jL_points2.setText(player2Nick+": "+player2Points);
        
    }
    
    private int textToInt(JButton button) {
        String text = button.getText();
        if(text.equals("X")) return 1;
        if(text.equals("O")) return 2;
        return 10;
    }
    
